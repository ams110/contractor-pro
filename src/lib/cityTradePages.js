// FAQPage JSON-LD (عربي — المحتوى الأساسي المفهرس) لصفحة معيّنة.
export function cityTradeFaqSchema(page) {
  if (!page || !page.faq) return null
  return {
    '@context': 'https://schema.org',
    '@type': 'FAQPage',
    mainEntity: page.faq.map(({ q, a }) => ({
      '@type': 'Question',
      name: q,
      acceptedAnswer: { '@type': 'Answer', text: a },
    })),
  }
}
